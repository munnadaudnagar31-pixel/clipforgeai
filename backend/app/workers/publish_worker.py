# â”€â”€ Main Celery Task â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
@celery_app.task(bind=True, name="publish_clip", max_retries=3)
def publish_clip_task(
    self,
    export_id: str,
    clip_cdn_url: Optional[str],
    clip_s3_key: Optional[str],
    platform: str,
    title: str,
    description: str = "",
    tags: list = None,
    user_id: str = "",
    scheduled_at: Optional[str] = None,
):
    """
    Celery task: publish a clip to a social platform.

    Args:
        export_id:    UUID of the Export DB record to update
        clip_cdn_url: CloudFront URL of the rendered clip
        clip_s3_key:  S3 key (fallback if no CDN URL)
        platform:     "youtube" | "tiktok" | "instagram" | "download"
        title:        Video title (already interpolated)
        description:  Caption / description
        tags:         List of hashtags
        user_id:      User UUID (for fetching OAuth tokens)
        scheduled_at: ISO8601 datetime to schedule (None = publish now)
    """
    if tags is None:
        tags = ["#Gaming", "#Shorts", "#ClipForge"]

    SessionLocal = _get_session()

    print(f"[PublishWorker] Starting export_id={export_id} platform={platform}")

    # â”€â”€ Handle scheduled future publish â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    if scheduled_at:
        try:
            scheduled_dt = datetime.fromisoformat(scheduled_at)
            now = datetime.now(timezone.utc)
            if scheduled_dt.tzinfo is None:
                from datetime import timezone as tz
                scheduled_dt = scheduled_dt.replace(tzinfo=tz.utc)
            if scheduled_dt > now:
                delay_s = (scheduled_dt - now).total_seconds()
                print(f"[PublishWorker] Scheduling for {scheduled_at} (in {delay_s:.0f}s)")
                publish_clip_task.apply_async(
                    kwargs={
                        "export_id": export_id,
                        "clip_cdn_url": clip_cdn_url,
                        "clip_s3_key": clip_s3_key,
                        "platform": platform,
                        "title": title,
                        "description": description,
                        "tags": tags,
                        "user_id": user_id,
                        "scheduled_at": None,   # don't reschedule again
                    },
                    countdown=int(delay_s),
                )
                _update_export(export_id, SessionLocal, status="scheduled")
                return {"status": "scheduled", "at": scheduled_at}
        except Exception as e:
            print(f"[PublishWorker] Schedule parse error: {e}")

    # â”€â”€ Direct publish â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
    if platform == "download":
        # No upload needed â€” user downloads directly
        _update_export(
            export_id, SessionLocal,
            status="published",
            published_at=datetime.utcnow(),
            published_url=clip_cdn_url or "",
        )
        return {"status": "published", "platform": "download"}

    with tempfile.TemporaryDirectory() as tmpdir:
        # Download clip locally
        video_path = str(Path(tmpdir) / "clip.mp4")

        try:
            source_url = clip_cdn_url
            if not source_url and clip_s3_key:
                # Build S3 URL fallback
                source_url = f"https://{settings.S3_BUCKET_RENDERED}.s3.{settings.AWS_REGION}.amazonaws.com/{clip_s3_key}"

            if not source_url:
                raise ValueError("No CDN or S3 URL available for clip download")

            _download_clip(source_url, video_path)

        except Exception as e:
            err = f"Download failed: {e}"
            print(f"[PublishWorker] {err}")
            _update_export(export_id, SessionLocal, status="failed", error_msg=err)
            raise self.retry(exc=e, countdown=60)

        # â”€â”€ Platform dispatch â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
        try:
            if platform == "youtube":
                result = _publish_youtube(video_path, title, description, tags, user_id)
                published_url = result["url"]

            elif platform == "tiktok":
                result = _publish_tiktok(video_path, title, tags, user_id)
                published_url = result["url"]

            elif platform == "instagram":
                result = _publish_instagram(video_path, title, tags, user_id)
                published_url = result["url"]

            else:
                raise ValueError(f"Unsupported platform: {platform}")

            # â”€â”€ Update DB â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
            _update_export(
                export_id, SessionLocal,
                status="published",
                published_url=published_url,
                published_at=datetime.utcnow(),
                platform_video_id=result.get("video_id") or result.get("media_id"),
            )
            print(f"[PublishWorker] Published to {platform}: {published_url}")
            return {"status": "published", "platform": platform, "url": published_url}

        except Exception as e:
            err = str(e)
            print(f"[PublishWorker] Publish failed: {err}")
            _update_export(export_id, SessionLocal, status="failed", error_msg=err[:500])
            raise self.retry(exc=e, countdown=120)

