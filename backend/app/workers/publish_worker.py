# â”€â”€ DB Helper â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def _get_session():
    url = settings.DATABASE_URL.replace("postgresql+asyncpg", "postgresql+psycopg2")
    engine = create_engine(url, pool_pre_ping=True)
    Session = sessionmaker(engine)
    return Session


def _update_export(export_id: str, session_factory, **kwargs):
    from app.models.models import Export
    with session_factory() as session:
        session.execute(
            update(Export).where(Export.id == export_id).values(**kwargs)
        )
        session.commit()


# â”€â”€ Download helper â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def _download_clip(cdn_url: str, dest_path: str) -> str:
    """Stream-download a clip from CDN to local file."""
    resp = requests.get(cdn_url, stream=True, timeout=120)
    resp.raise_for_status()
    with open(dest_path, "wb") as f:
        for chunk in resp.iter_content(chunk_size=8192):
            f.write(chunk)
    return dest_path


